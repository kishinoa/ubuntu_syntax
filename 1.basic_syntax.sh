# 현재 위치 경로 출력
pwd

# 현재위치에서 파일 및 폴더 조회
ls

# 목록조회 자세히
ls -l

# 목록조회 자세히(숨김 파일까지)
ls -al

# 목록조회 자세히, 숨김파일, 오래된순정렬(최신순 alt)
ls -alrt

